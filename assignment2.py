Days = float(input("Enter the number in the range of 1 through 7: "))
if Days == 1:
    print("Monday")
elif Days == 2:
    print("Tuesday")
elif Days == 3:
    print("Wednesday")
elif Days == 4: 
    print("Thursday")
elif Days == 5:
    print("Friday")
elif Days == 6:
    print("Saturday")
elif Days == 7:
    print("Sunday")
else:
    print("Invalid input. Please enter a valid day number.")